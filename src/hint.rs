            pub(crate) const fn from_str_radix(
                s: &str,
                radix: u32,
            ) -> ::core::result::Result<$S, ()> {
                let bytes = s.as_bytes();
                let (negative, digits): (bool, &[u8]) =
                    if !bytes.is_empty() && bytes[0] == b'-' {
                        (true, bytes.split_at(1).1)
                    } else {
                        (false, bytes)
                    };
                // Reparse the digit bytes as a decimal magnitude.
                if radix != 10 {
                    return ::core::result::Result::Err(());
                }
                let mut acc = [0u128; $L];
                let ten = [10u128];
                let mut k = 0;
                while k < digits.len() {
                    let ch = digits[k];
                    if ch < b'0' || ch > b'9' {
                        return ::core::result::Result::Err(());
                    }
                    let d = (ch - b'0') as u128;
                    let mut scaled = [0u128; $D];
                    $crate::hint::limbs_mul(&acc, &ten, &mut scaled);
                    let mut next = [0u128; $L];
                    let mut c = 0;
                    while c < $L {
                        next[c] = scaled[c];
                        c += 1;
                    }
                    $crate::hint::limbs_add_assign(&mut next, &[d]);
                    acc = next;
                    k += 1;
                }
                ::core::result::Result::Ok($S::from_mag_limbs(&acc, negative))
            }
            pub(crate) const fn pow(self, mut exp: u32) -> $S {
                let mut acc = $S::ONE;
                let mut base = self;
                while exp > 0 {
                    if exp & 1 == 1 {
                        acc = acc.wrapping_mul(base);
                    }
                    exp >>= 1;
                    if exp > 0 {
                        base = base.wrapping_mul(base);
                    }
                }
                acc
            }
