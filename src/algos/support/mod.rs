pub(crate) mod fixed;
pub(crate) mod mg_divide;

// Per-scale, oracle-sourced, width-deduplicated transcendental
// constant table (pi/tau/half_pi/quarter_pi/e/golden/ln2/ln10/
// deg_per_rad/rad_per_deg). Generated by `scripts/gen_const_table.py`
// (mpmath oracle), committed, NOT built at build time.
//
// The module is ALWAYS compiled: the public `DecimalConstants` trait on
// the narrow tiers (D18 = Int<1>, D38 = Int<2>) sources its constants
// from the always-present NARROW band (scales 0..=38) in every build,
// including default and `--no-default-features` (no_std). The three
// WIDER scale bands (BASE / XW / XXW, used by the wide tiers) stay
// feature-gated inside the file behind `_wide-support` / `x-wide` /
// `xx-wide`, so a narrow-only build carries only the few-KB narrow band.
pub(crate) mod const_table;

// Typed-`W` Newton-root seed bridge over the cross-algorithm seed leaf
// (`algo_x_support::seed`). Used by the wide fixed-point `sqrt` kernel,
// which needs the over-estimate seed (not the final floor root) to start
// its own scaled Newton loop. The floor-root surface itself is
// `W::isqrt` / `W::icbrt`.
pub(crate) mod seed_bridge;

// Tier-generic surface over the per-tier wide guard-digit cores. The
// `WideTrigCore` trait + six `*_series` functions collapse the 60
// per-tier `*_strict_<tier>` wrappers in `algos::{exp,ln,trig}::wide_kernel`
// to one generic function per transcendental. Always compiled; the
// per-tier `impl WideTrigCore` blocks are emitted by
// `decl_wide_transcendental!`.
pub(crate) mod wide_trig_core;

// Baked binary Tang `ln(1 + i/M)` lookup table (`M = 128`) + the
